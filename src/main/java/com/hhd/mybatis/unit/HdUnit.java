package com.hhd.mybatis.unit;

public class HdUnit {
	private String hdUnitCode;
	private String hdUnitName;
	private String hdUnitEngName;
	private String hdUnitStatus;
	private String hdNotes;
	public String getHdUnitCode() {
		return hdUnitCode;
	}
	public void setHdUnitCode(String hdUnitCode) {
		this.hdUnitCode = hdUnitCode;
	}
	public String getHdUnitName() {
		return hdUnitName;
	}
	public void setHdUnitName(String hdUnitName) {
		this.hdUnitName = hdUnitName;
	}

	public String getHdUnitStatus() {
		return hdUnitStatus;
	}
	public void setHdUnitStatus(String hdUnitStatus) {
		this.hdUnitStatus = hdUnitStatus;
	}
	public String getHdUnitEngName() {
		return hdUnitEngName;
	}
	public void setHdUnitEngName(String hdUnitEngName) {
		this.hdUnitEngName = hdUnitEngName;
	}
	public String getHdNodes() {
		return hdNotes;
	}
	public void setHdNodes(String hdNotes) {
		this.hdNotes = hdNotes;
	}
	
	

}
